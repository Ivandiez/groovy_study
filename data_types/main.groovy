#!groovy

class Example {
  static void main(String[] args) {
    // Using a simple println statement to print output to the console
    println('Hello World');

    // Int datatype
    int x = 5;

    // Long datatype
    long y = 100L;
    
    // Floating point datatype
    float a = 10.56f;

    // Double datatype
    double b = 10.5e40;

    // BigInteger datatype
    BigInteger bi = 30g;

    // BigDecimal datatype
    BigDecimal bd = 3.5g;

    println(x);
    println(y);
    println(a);
    println(b);
    println(bi);
    println(bd);
  }
}
